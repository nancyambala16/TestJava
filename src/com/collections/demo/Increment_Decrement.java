
package com.collections.demo;

public class Increment_Decrement {
	
	public static void main(String[] args) {
		
		int i=1;
		i++;
		System.out.println(i);
		
		System.out.println("**********");
		i--;
		System.out.println(i);
		
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(--i);
	
		
	}

}
